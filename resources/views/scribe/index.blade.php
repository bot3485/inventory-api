        <h1 id="introduction">Introduction</h1>
<p>Official API documentation for the Inventory Management System.</p>
<aside>
    <strong>Base URL</strong>: <code>http://172.30.10.12:9443</code>
</aside>
<p>Welcome to the Inventory System API.
All requests must include the <code>Accept: application/json</code> header.
Use the Bearer token obtained during login for authentication.</p>

        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer {YOUR_AUTH_TOKEN}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>Retrieve your token via the <b>/api/login</b> endpoint.</p>

